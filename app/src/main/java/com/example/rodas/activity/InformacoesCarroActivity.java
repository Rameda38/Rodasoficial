    private boolean validarCamposCarrosOpcoes() {
        if (marcaCar == "MARCA" || modeloCar == "MODELO") {
            Toast.makeText(getApplicationContext(),
                    "FALTA SELECIONAR UM /n DOS DADOS PFV VERIFICAR",Toast.LENGTH_SHORT).show();
            return false;
        }else {
            return true;
        }
    }

