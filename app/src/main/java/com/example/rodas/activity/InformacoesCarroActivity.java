    public void SalvarCarrosOpcoes(View view) {
        if (validarCamposCarrosOpcoes()==true) {

            carro = new Carro();
            String modeloCar = Modelo.getSelectedItem().toString();
            Toast.makeText(getApplicationContext(),""+modeloCar,Toast.LENGTH_SHORT).show();
            String marcaCar = Marca.getSelectedItem().toString();
            Toast.makeText(getApplicationContext(),""+marcaCar,Toast.LENGTH_SHORT).show();
            String anoFab = AnoFab.getSelectedItem().toString();
            Toast.makeText(getApplicationContext(),""+anoFab,Toast.LENGTH_SHORT).show();

            carro.setModelo(modeloCar);
            carro.setMarca(marcaCar);
            carro.setAnoFab(anoFab);

            carro.salvarCarros();

            finish();
        }
    }
